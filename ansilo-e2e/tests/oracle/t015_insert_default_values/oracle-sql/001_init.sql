EXCEPTION
WHEN OTHERS THEN NULL;
END;
$$

BEGIN
EXECUTE IMMEDIATE 'DROP TABLE T015__GENERATED_DEFAULT';
EXCEPTION
WHEN OTHERS THEN NULL;
END;
$$

BEGIN
EXECUTE IMMEDIATE 'DROP TABLE T015__DEFAULT';
EXCEPTION
WHEN OTHERS THEN NULL;
END;
$$

CREATE TABLE T015__GENERATED_ALWAYS (
    ID INT GENERATED ALWAYS AS IDENTITY,
    DATA VARCHAR(255)
)
$$

CREATE TABLE T015__GENERATED_DEFAULT (
    ID INT GENERATED BY DEFAULT AS IDENTITY,
    DATA VARCHAR(255)
)
$$

CREATE TABLE T015__DEFAULT (
    ID INT DEFAULT -1,
    DATA VARCHAR(255)
)
